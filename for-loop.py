# for <variable> in <sequence>:

colors = ["blue", "green", "red", "yellow"]

for color in colors:
    print(color)

point = (1, 2, 3)
for value in point:
    print(value)

ages = {'kevin': 50, 'bob': 40, 'kayla': 21}
for key, value in ages.items():
    print(key, value)
    
for key in ages:
    print(key)

